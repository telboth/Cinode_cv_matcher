def _payload_fingerprint(text: str) -> str:
    value = (text or "").strip()
    if not value:
        return "empty"
    digest = hashlib.sha1(value.encode("utf-8")).hexdigest()[:10]
    preview = " ".join(value.split())[:40]
    return f"len={len(value)} sha1={digest} preview='{preview}'"


def _compose_presentation_text(payload: dict[str, Any]) -> str:
    summary = payload.get("summary")
    if isinstance(summary, str) and summary.strip():
        return summary.strip()[:12000]
    return ""


def _normalize_title_for_match(value: str) -> str:
    return " ".join((value or "").strip().lower().split())


def _ensure_unique_resume_title(title: str, resumes: list[dict[str, Any]] | None) -> str:
    wanted = (title or "").strip()
    if not wanted:
        return wanted
    existing: set[str] = set()
    for row in resumes or []:
        if not isinstance(row, dict):
            continue
        row_title = str(row.get("title") or "").strip()
        if row_title:
            existing.add(_normalize_title_for_match(row_title))
    wanted_key = _normalize_title_for_match(wanted)
    if wanted_key not in existing:
        return wanted
    suffix = datetime.now().strftime("%Y-%m-%d %H.%M.%S")
    return f"{wanted} ({suffix})"


def _collect_skill_terms_from_suggestions(suggestions: list[CvSuggestion]) -> list[str]:
    accepted = [s for s in suggestions if str(s.status or "").strip().lower() == "accepted"]
    skill_types = {
        "keyword",
        "skills",
        "skill",
        "tools",
        "tool",
        "techniques",
        "technique",
        "teknikker",
        "teknikk",
        "verktøy",
        "verktoy",
        "kompetanse",
    }
    raw_terms: list[str] = []
    for item in accepted:
        section = str(item.section_type or "").strip().lower()
        if section not in skill_types:
            continue
        text = str(item.suggested_text or "").strip()
        if not text:
            continue
        for chunk in re.split(r"[\n,;|]+", text):
            term = chunk.strip(" -•\t\r\n")
            if not term:
                continue
            raw_terms.append(term)

    def _normalize_skill_term(term: str) -> str:
        t = " ".join((term or "").split()).strip()
        low = t.lower()
        # Strip likely autogenerated noise suffixes, e.g. "skill21665174174146".
        # Keep normal version numbers like "C++20" or "ISO 27001" intact.
        def _strip_generated_suffix(value: str) -> str:
            txt = value.strip()
            # Case 1: explicit separator before a long numeric tail.
            m = re.match(r"^(.*?)[\s_\-]+\d{8,}$", txt)
            if m and m.group(1).strip():
                return m.group(1).strip()
            # Case 2: long numeric tail attached directly to letters.
            m2 = re.match(r"^(.*?[A-Za-zÆØÅæøå])\d{8,}$", txt)
            if m2 and m2.group(1).strip():
                return m2.group(1).strip()
            return txt

        t = _strip_generated_suffix(t)
        low = t.lower()
        if low.endswith("erfaring") and len(low) > len("erfaring") + 2:
            stem = t[: -len("erfaring")].strip(" -_/")
            if stem:
                t = stem
                low = t.lower()
        # Common Norwegian stem cleanup after stripping "...erfaring"
        # e.g. "programmerings" -> "programmering"
        if low.endswith("ings") and len(low) > 6:
            t = t[:-1]
            low = t.lower()
        if low == "programmerings":
            t = "Programmering"
            low = "programmering"
        # keep common preferred spelling for this project
        if low in {"claude code", "claude-code"}:
            t = "Claude-code"
        return t

    out: list[str] = []
    seen: set[str] = set()
    for term in raw_terms:
        normalized = _normalize_skill_term(term)
        norm = " ".join(normalized.split()).lower()
        if len(norm) < 2 or len(norm) > 80:
            continue
        if norm in seen:
            continue
        seen.add(norm)
        out.append(normalized)
    return out[:20]


def _extract_skills_from_selected_resume(resume_payload: dict[str, Any] | None) -> list[str]:
    if not isinstance(resume_payload, dict):
        return []
    resume = resume_payload.get("resume")
    if not isinstance(resume, dict):
        return []
    blocks = resume.get("blocks")
    if not isinstance(blocks, list):
        return []

    out: list[str] = []
    seen: set[str] = set()
    for block in blocks:
        if not isinstance(block, dict):
            continue
        if int(block.get("blockType") or 0) != 12:
            continue
        data = block.get("data")
        if not isinstance(data, list):
            continue
        for category in data:
            if not isinstance(category, dict):
                continue
            skills = category.get("skills")
            if not isinstance(skills, list):
                continue
            for skill in skills:
                if not isinstance(skill, dict):
                    continue
                name = str(skill.get("name") or "").strip()
                if not name:
                    continue
                key = name.lower()
                if key in seen:
                    continue
                seen.add(key)
                out.append(name)
    return out[:80]


