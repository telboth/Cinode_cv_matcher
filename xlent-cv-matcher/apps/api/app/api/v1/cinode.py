def _resolve_auth_value(row: CinodeCredential, token_override: str | None) -> str:
    override = (token_override or "").strip()
    if override:
        return normalize_auth_value(override)
    if (row.label or "").strip() == "Cinode (.env)":
        live = (get_live_secret_value("CINODE_API_TOKEN") or "").strip()
        if live:
            return normalize_auth_value(live)
    return row.auth_value


def _find_selected_resume_entry(consultant_data: dict[str, Any]) -> dict[str, Any] | None:
    selected_resume_id = consultant_data.get("selected_resume_id")
    resumes = consultant_data.get("resumes")
    if not isinstance(resumes, list):
        return None
    for entry in resumes:
        if not isinstance(entry, dict):
            continue
        entry_id = entry.get("id")
        try:
            if entry_id is not None and selected_resume_id is not None and int(entry_id) == int(selected_resume_id):
                return entry
        except Exception:
            continue
    return None


def _ensure_env_credential(db: Session) -> None:
    settings = get_settings()
    base_url = (settings.cinode_base_url or "").strip()
    raw_auth = (settings.cinode_api_token or "").strip()
    if not base_url or not raw_auth:
        return

    normalized_auth = normalize_auth_value(raw_auth)
    env_label = "Cinode (.env)"

    row = db.query(CinodeCredential).filter(CinodeCredential.label == env_label).first()
    if row:
        changed = False
        if row.base_url != base_url:
            row.base_url = base_url
            changed = True
        if row.auth_value != normalized_auth:
            row.auth_value = normalized_auth
            changed = True
        if not row.is_default:
            db.query(CinodeCredential).update({CinodeCredential.is_default: False})
            row.is_default = True
            changed = True
        if changed:
            db.commit()
        return

    db.query(CinodeCredential).update({CinodeCredential.is_default: False})
    row = CinodeCredential(
        label=env_label,
        base_url=base_url,
        auth_value=normalized_auth,
        is_default=True,
    )
    db.add(row)
    db.commit()


@router.get("/credentials", response_model=list[CinodeCredentialRead])
def list_credentials(db: Session = Depends(get_db)) -> list[CinodeCredentialRead]:
    _ensure_env_credential(db)
    rows = db.query(CinodeCredential).order_by(CinodeCredential.created_at.desc()).all()
    return [_to_read(row) for row in rows]


