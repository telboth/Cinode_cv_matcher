@router.get("/credentials/{credential_id}/consultants/{consultant_id}/cv.browser-pdf")
def download_consultant_cv_browser_pdf(
    credential_id: str,
    consultant_id: str,
    resume_id: int | None = None,
    prefer_public_url: bool = True,
    token_override: str | None = None,
    db: Session = Depends(get_db),
) -> Response:
    row = db.query(CinodeCredential).filter(CinodeCredential.id == credential_id).first()
    if not row:
        raise HTTPException(status_code=404, detail="Credential not found")
    auth_value = _resolve_auth_value(row, token_override)

    try:
        result = fetch_cinode_consultant_cv(
            base_url=row.base_url,
            auth_value=auth_value,
            consultant_id=consultant_id,
            resume_id=resume_id,
        )
    except CinodeApiError as exc:
        raise HTTPException(status_code=400, detail=str(exc)) from exc

    selected_resume_id = result.get("selected_resume_id")
    selected_resume: dict | None = None
    resumes = result.get("resumes")
    if isinstance(resumes, list):
        for item in resumes:
            if not isinstance(item, dict):
                continue
            item_id = item.get("id")
            if item_id is not None and selected_resume_id is not None and int(item_id) == int(selected_resume_id):
                selected_resume = item
                break
        if selected_resume is None and resumes:
            first = resumes[0]
            if isinstance(first, dict):
                selected_resume = first

    candidate_urls: list[str] = []
    if selected_resume:
        public_url = selected_resume.get("publicViewUrl")
        view_url = selected_resume.get("viewUrl")
        public_value = public_url.strip() if isinstance(public_url, str) else ""
        view_value = view_url.strip() if isinstance(view_url, str) else ""
        if prefer_public_url:
            if public_value:
                candidate_urls.append(public_value)
            if view_value and view_value not in candidate_urls:
                candidate_urls.append(view_value)
        else:
            if view_value:
                candidate_urls.append(view_value)
            if public_value and public_value not in candidate_urls:
                candidate_urls.append(public_value)

    if not candidate_urls:
        raise HTTPException(status_code=400, detail="Could not resolve resume view URL for browser export")

    last_error: str | None = None
    pdf_bytes: bytes | None = None
    for url in candidate_urls:
        try:
            pdf_bytes = render_cv_page_to_pdf(cv_url=url)
            break
        except CinodeBrowserExportError as exc:
            last_error = str(exc)

    if pdf_bytes is None:
        detail = last_error or "Browser export failed"
        detail += " (Tip: ensure the resume is shared as public/Anyone in Cinode for headless export.)"
        raise HTTPException(status_code=400, detail=detail)

    safe_name = result["full_name"].strip().replace(" ", "_") or f"consultant_{consultant_id}"
    filename = f"{safe_name}_cinode_layout.pdf"
    headers = {"Content-Disposition": f'attachment; filename="{filename}"'}
    return Response(content=pdf_bytes, media_type="application/pdf", headers=headers)


